var sportsOne = ['Golf', 'Cricket', 'Football', 'Handball', 'Basketball'];
// SIMPLE FOR LOOPS
for (var i = 0; i < sportsOne.length; i++) {
    console.log(sportsOne[i]);
}
console.log('End of Simple For Loop');
// SIMPLIFIED FOR LOOP
for (var _i = 0, sportsOne_1 = sportsOne; _i < sportsOne_1.length; _i++) {
    var eachSport = sportsOne_1[_i];
    console.log(eachSport);
}
console.log('End of SIMPLIFIED For Loop');
